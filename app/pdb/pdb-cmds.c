static GimpValueArray *
pdb_get_data_invoker (GimpProcedure         *procedure,
                      Gimp                  *gimp,
                      GimpContext           *context,
                      GimpProgress          *progress,
                      const GimpValueArray  *args,
                      GError               **error)
{
    gboolean success = TRUE;
    GimpValueArray *return_vals;
    const gchar *identifier;
    gint bytes = 0;
    guint8 *data = NULL;

    identifier = g_value_get_string (gimp_value_array_index (args, 0));

    if (success)
    {
        if (gimp_is_canonical_identifier (identifier))
        {
            const guint8 *orig_data;

            orig_data = gimp_plug_in_manager_get_data (gimp->plug_in_manager,
                        identifier, &bytes);

            if (orig_data)
                data = g_memdup (orig_data, bytes);
            else
                success = FALSE;
        }
        else
        {
            g_set_error (error, GIMP_PDB_ERROR, GIMP_PDB_ERROR_INVALID_ARGUMENT,
                         _("Data label '%s' is not a canonical identifier"),
                         identifier);
            success = FALSE;
        }
    }

    return_vals = gimp_procedure_get_return_values (procedure, success,
                  error ? *error : NULL);

    if (success)
    {
        g_value_set_int (gimp_value_array_index (return_vals, 1), bytes);
        gimp_value_take_uint8_array (gimp_value_array_index (return_vals, 2), data, bytes);
    }

    return return_vals;
}

static GimpValueArray *
pdb_get_data_size_invoker (GimpProcedure         *procedure,
                           Gimp                  *gimp,
                           GimpContext           *context,
                           GimpProgress          *progress,
                           const GimpValueArray  *args,
                           GError               **error)
{
    gboolean success = TRUE;
    GimpValueArray *return_vals;
    const gchar *identifier;
    gint bytes = 0;

    identifier = g_value_get_string (gimp_value_array_index (args, 0));

    if (success)
    {
        if (gimp_is_canonical_identifier (identifier))
        {
            if (! gimp_plug_in_manager_get_data (gimp->plug_in_manager,
                                                 identifier, &bytes))
                success = FALSE;
        }
        else
        {
            g_set_error (error, GIMP_PDB_ERROR, GIMP_PDB_ERROR_INVALID_ARGUMENT,
                         _("Data label '%s' is not a canonical identifier"),
                         identifier);
            success = FALSE;
        }
    }

    return_vals = gimp_procedure_get_return_values (procedure, success,
                  error ? *error : NULL);

    if (success)
        g_value_set_int (gimp_value_array_index (return_vals, 1), bytes);

    return return_vals;
}

